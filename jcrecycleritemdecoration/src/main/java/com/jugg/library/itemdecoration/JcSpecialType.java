public class JcSpecialType {

    private int type;
    private int top;
    private int left;
    private int right;
    private int bottom;


    public JcSpecialType(int type, int top, int left, int right, int bottom) {
        this.type = type;
        this.top = top;
        this.left = left;
        this.right = right;
        this.bottom = bottom;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public int getBottom() {
        return bottom;
    }

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }
}
